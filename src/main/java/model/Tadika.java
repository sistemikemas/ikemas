package model;

public class Tadika {

    private String kodtadika;
    private String namatadika;
    private String alamat;
    private int bilangankelas;
    private String sesipersekolahan;
    private String parlimen;
    private String dun;
    private int idpenyelia;

    public Tadika() {
    }

    // Getter Setter
    public String getKodtadika() {
        return kodtadika;
    }

    public void setKodtadika(String kodtadika) {
        this.kodtadika = kodtadika;
    }

    public String getNamatadika() {
        return namatadika;
    }

    public void setNamatadika(String namatadika) {
        this.namatadika = namatadika;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public int getBilangankelas() {
        return bilangankelas;
    }

    public void setBilangankelas(int bilangankelas) {
        this.bilangankelas = bilangankelas;
    }

    public String getSesipersekolahan() {
        return sesipersekolahan;
    }

    public void setSesipersekolahan(String sesipersekolahan) {
        this.sesipersekolahan = sesipersekolahan;
    }

    public String getParlimen() {
        return parlimen;
    }

    public void setParlimen(String parlimen) {
        this.parlimen = parlimen;
    }

    public String getDun() {
        return dun;
    }

    public void setDun(String dun) {
        this.dun = dun;
    }

    public int getIdpenyelia() {
        return idpenyelia;
    }

    public void setIdpenyelia(int idpenyelia) {
        this.idpenyelia = idpenyelia;
    }
}
